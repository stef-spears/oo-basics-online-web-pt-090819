class Book
  
  def initialize(title, author, page_count, genre)
    @title = title
    @author = author
    @page_count = page_count
    @genre = genre
  end
  
  def title 
    @title 
  end 
  
  def author
    @author
  end 
  
  def page_count
    @page_count
  end 
  
  def genre 
    @genre 
  end
  
end 

